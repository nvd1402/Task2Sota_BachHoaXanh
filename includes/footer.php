    <script>
    document.addEventListener('DOMContentLoaded', function() {
        const newsletterForm = document.getElementById('newsletter-form');
        const newsletterEmail = document.getElementById('newsletter-email');
        const newsletterSubmitBtn = document.getElementById('newsletter-submit-btn');
        
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                
                const email = newsletterEmail.value.trim();
                
                if (!email) {
                    showToast('Vui lòng nhập địa chỉ email', 'error');
                    return;
                }
                
                if (!validateEmail(email)) {
                    showToast('Địa chỉ email không hợp lệ', 'error');
                    return;
                }
                
                // Disable button while submitting
                newsletterSubmitBtn.disabled = true;
                newsletterSubmitBtn.textContent = 'Đang xử lý...';
                
                // Send AJAX request
                const formData = new FormData();
                formData.append('email', email);
                
                fetch('newsletter_subscribe.php', {
                    method: 'POST',
                    body: formData
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        showToast(data.message, 'success');
                        newsletterEmail.value = '';
                    } else {
                        showToast(data.message, 'error');
                    }
                })
                .catch(error => {
                    showToast('Có lỗi xảy ra. Vui lòng thử lại sau.', 'error');
                    console.error('Error:', error);
                })
                .finally(() => {
                    newsletterSubmitBtn.disabled = false;
                    newsletterSubmitBtn.textContent = 'Đăng ký';
                });
            });
        }
        
        function showToast(message, type) {
            const container = document.getElementById('toast-container');
            if (!container) return;
            
            // Tạo toast element
            const toast = document.createElement('div');
            toast.className = `toast-notification ${type}`;
            
            // Icon và nội dung
            const icon = type === 'success' ? '✓' : '✕';
            const title = type === 'success' ? 'Thành công!' : 'Lỗi!';
            
            toast.innerHTML = `
                <div class="toast-icon">${icon}</div>
                <div class="toast-content">
                    <div class="toast-title">${title}</div>
                    <div class="toast-message">${message}</div>
                </div>
                <button class="toast-close" onclick="this.parentElement.remove()">&times;</button>
            `;
            
            // Thêm vào container
            container.appendChild(toast);
            
            // Tự động xóa sau 5 giây
            setTimeout(() => {
                toast.classList.add('hiding');
                setTimeout(() => {
                    if (toast.parentElement) {
                        toast.remove();
                    }
                }, 300);
            }, 5000);
        }
        
        function validateEmail(email) {
            const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            return re.test(email);
        }
    });
